            fetch('/admin/peminjaman/check-overdue', {
                method: 'POST',
                headers: {
                    'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]').getAttribute(
                        'content')
                }
            }).catch(function(error) {
                console.log('Background check error:', error);
            });
        }, 300000);
